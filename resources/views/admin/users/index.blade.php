                $('.fetchButton').click(function() {
                    var route = $(this).data('route');
                    var buttonId = $(this).attr('id');
                    var loadingSvg = $('#' + buttonId + 'Loading'); // Get the specific loading SVG
                    var iconSvg = $('#' + buttonId + 'Icon'); // Get the specific icon SVG

                    // Show loading SVG when the button is clicked
                    loadingSvg.removeClass('hidden');
                    iconSvg.addClass('hidden');

                    // Make an AJAX request to trigger the controller method for the selected route
                    $.ajax({
                        type: 'GET',
                        url: route,
                        success: function(response) {
                            if (response.success) {
                                alert(response.message);
                            }
                        },
                        error: function(xhr, status, error) {
                            var errorMessage = xhr.responseJSON && xhr.responseJSON.error ? xhr
                                .responseJSON.error.message : 'Unknown error occurred.';
                            alert('Error: ' + errorMessage);
                        },
                        complete: function() {
                            // Hide loading SVG when the AJAX request is complete
                            loadingSvg.addClass('hidden');
                            iconSvg.removeClass('hidden');
                        }
                    });
                });
